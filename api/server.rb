get '/artworks' do
  limit = params['limit'].nil? ? 100 : Integer(params['limit'])
  offset = params['offset'].nil? ? 0 : Integer(params['offset'])
  dominant_colors = params['dominant_colors']

  if dominant_colors && !dominant_colors.is_a?(Array)
    halt 400, "Bad Request"
  end

  if limit.negative? || offset.negative?
    halt 400, "Bad Request"
  end

  if dominant_colors
    res = artwork_data.select{ |artwork| dominant_colors.include?(artwork[:dominant_color]) }
  else
    res = artwork_data
  end

  json res.slice(offset.to_i, limit.to_i)
end

get '/artist' do
  if params['id'].nil?
    halt 400, "Bad Request"
  end

  artist_id = Integer(params['id'])

  artist = artist_data.select { |artist| artist[:id] == artist_id }

  json artist
end